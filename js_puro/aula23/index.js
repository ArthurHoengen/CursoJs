/* 
Operadores lógicos
&&  -> AND  -> E    -> Todas as expressões precisam ser verdadeiras
||  -> OR   -> OU   -> Uma das expressões precisa ser verdadeira
!   -> NOT  -> NÃO  -> Inverte uma expressão
*/

console.log(true && true);
console.log(true && false);
console.log(false || true);

console.log(!false, !true);